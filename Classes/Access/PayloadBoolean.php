namespace NITSAN\NsT3AF\Access;

/**
 * Parses booleans from AJAX payloads (FormData sends "false"/"true" strings).
 */
final class PayloadBoolean
{
    public static function parse(mixed $value): bool
    {
        if (is_bool($value)) {
            return $value;
        }

        if (is_int($value) || is_float($value)) {
            return $value !== 0;
        }

        if (is_string($value)) {
            $parsed = filter_var($value, FILTER_VALIDATE_BOOLEAN, FILTER_NULL_ON_FAILURE);
            if ($parsed !== null) {
                return $parsed;
            }
        }

        return (bool) $value;
    }
}
